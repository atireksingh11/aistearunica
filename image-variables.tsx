export type ImageKey = keyof typeof SITE_IMAGES

interface ResponsiveImageProps {
  src: ImageKey | string
  alt: string
  className?: string
  width?: number
  height?: number
  priority?: boolean
}

export function ResponsiveImage({ src, alt, className = "", width, height, priority = false }: ResponsiveImageProps) {
  const imageSrc = typeof src === "string" && src in SITE_IMAGES ? SITE_IMAGES[src as ImageKey] : src

  return (
    <img
      src={imageSrc || "/placeholder.svg"}
      alt={alt}
      className={`transition-all duration-300 ${className}`}
      width={width}
      height={height}
      loading={priority ? "eager" : "lazy"}
    />
  )
}
